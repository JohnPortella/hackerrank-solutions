def bitwiseAnd(N, K):
    # Write your code here
    max_operator = -1
    for a in range(K-2, N):
        for b in range(a+1, N+1):
            operator = a & b
            if operator == K-1:
                return operator
            if operator < K and operator > max_operator:
                max_operator = operator
    return max_operator
    

